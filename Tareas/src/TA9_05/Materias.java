package TA9_05;

public class Materias {
	private String NombreMateria;

	public Materias(String nombreMateria) {
		this.NombreMateria = nombreMateria;
	}
	//matematicas
	public String Matematicas(String Matematicas) {
        return this.NombreMateria = Matematicas;
    }
	//filosofia
	public String Filosofia(String Filosofia) {
		return this.NombreMateria = Filosofia;
	}
	//fisica
	public String Fisica(String Fisica) {
		return this.NombreMateria = Fisica;
	}

	public String getNombreMateria() {
		return NombreMateria;
	}
	
}
